    private void btn_modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifierActionPerformed
        int vid = JOptionPane.showConfirmDialog(null, "Voulez-vous Modifier ce Apprenant ?", "MODIFICATION APPRENANT", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (vid == JOptionPane.YES_OPTION) {
            if (tb_recherche_app.getText().equalsIgnoreCase("") || (!tb_recherche_app.getText().equalsIgnoreCase(tb_matricule.getText()))) {
                JOptionPane.showMessageDialog(null, "Le matricule à modifier svp");
            } else {
                Apprenants appren = new Apprenants();
                appren.setMatricule(tb_matricule.getText());
                appren.setNom(tb_nom.getText());
                appren.setPrenom(tb_prenom.getText());
                appren.setGenre(cbo_genre.getSelectedItem().toString());
                appren.setTel(tb_tel.getText());
                appren.setChemin_photo(path);

                int modif = new IMP_Apprenants().Modifier_apprenant(appren, tb_recherche_app.getText(), true);
                if (modif != 0) {
                    JOptionPane.showMessageDialog(null, "Modification effectuée avec succès");
                    vider_champs_apprenant();
                    new IMP_Apprenants().liste_des_apprenants(jTable_Apprenant);
                } else {
                    JOptionPane.showMessageDialog(null, "Modification echouée");
                }
            }
        }

    }//GEN-LAST:event_btn_modifierActionPerformed
