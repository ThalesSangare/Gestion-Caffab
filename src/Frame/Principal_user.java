    public void monguide() {

        File fichier = new File("src/Etats/GUIDE_GESTION_CAFFAB.pdf");
// On vérifie que la classe Desktop soit bien supportée : 
        if (Desktop.isDesktopSupported()) {
            // On récupère l'instance du desktop : 
            Desktop desktop = Desktop.getDesktop();
            // On vérifie que la fonction open est bien supportée : 
            if (desktop.isSupported(Desktop.Action.OPEN)) {
                // Et on lance l'application associé au fichier pour l'ouvrir : 
                try {
                    desktop.open(fichier);
                } catch (Exception e) {
                }
            }
        }
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            InputStream fichier, logo;
            int id_inscrip = new IMP_Inscription().get_id_inscrip_selon_matri_et_cours(cbo_matricule.getSelectedItem().toString(), cbo_cours.getSelectedItem().toString());
//            int montant_total = new ImpTranche().get_montantTotal_tranche(CboNiveau.getSelectedItem().toString());

            //----Pour recuperer les objet en parametre
            Map<String, Object> parametre = new HashMap<>();
            ///---Prend le chemin de l'image
            logo = new FileInputStream("src/images/logo.png");
            parametre.put("para_logo", logo);
            String code_qr = "Matricule " + cbo_matricule.getSelectedItem() + "\nNom " + tb_nom.getText() + "\nPrénom " + tb_prenom.getText() + "\nGenre "
                    + cbo_genre.getSelectedItem().toString();
            parametre.put("para_codeQr", code_qr);
            parametre.put("para_id_inscrip", id_inscrip);
            ///////----Prend le chemin de l'etat à imprimer
            fichier = new FileInputStream("src/Etats/Etat_Facture.jasper");
            JasperPrint pr = JasperFillManager.fillReport(fichier, parametre, new Connexion().getConnection());
            JasperViewer.viewReport(pr, true);
//            parametre.put("para_montant_scolaire", montant_total);

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, " erreur impression2 " + ex.getLocalizedMessage());
        } catch (JRException ex) {
            Logger.getLogger(Principal_user.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

