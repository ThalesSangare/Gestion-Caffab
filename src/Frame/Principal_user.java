    private void btn_ajout_inscripActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajout_inscripActionPerformed
        int verif = new IMP_Inscription().Verification_apprenant_inscrit(cbo_matricule.getSelectedItem().toString(), cbo_cours.getSelectedItem().toString());
        if (cbo_type_inscrip.getSelectedItem().toString().equalsIgnoreCase("Selectionner")) {
            JOptionPane.showMessageDialog(null, "Le Type est Obligatoire");
        } else if (cbo_matricule.getSelectedItem().toString().equalsIgnoreCase("Selectionner")) {
            JOptionPane.showMessageDialog(null, "Le Matricule est Obligatoire");
        } else if (cbo_cours.getSelectedItem().toString().equalsIgnoreCase("Selectionner")) {
            JOptionPane.showMessageDialog(null, "Le Cours est Obligatoire");
        }else if(tb_montant_payer.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Le montant payer est obligatoire");
            tb_montant_payer.requestFocus();
        }
        else if (verif != 0) {
            JOptionPane.showMessageDialog(null, "Cet apprenant est deja inscrit en cours de " + new IMP_Cours().Get_nom_cours_selon_id(verif));
        } else {
            int id_facture = new IMP_FACTURE().Enregistrer_facture(Integer.parseInt(tb_netApayer.getText()), Integer.parseInt(tb_montant_payer.getText()), Integer.parseInt(tb_reste_a_payer.getText()));
            boolean enreg = new IMP_Inscription().InscriptionApprenant(cbo_matricule.getSelectedItem().toString(), cbo_cours.getSelectedItem().toString(), cbo_cours.getSelectedItem().toString(), cbo_type_inscrip.getSelectedItem().toString(), cbo_type_inscrip.getSelectedItem().toString(), id_facture);
            if (enreg) {
                if (cbo_type_inscrip.getSelectedItem().toString().equalsIgnoreCase("INSCRIPTION")) {
                    JOptionPane.showMessageDialog(null, "Inscription faite avec succès");
                    vider_champs_inscription();
                    new IMP_Inscription().liste_des_inscrits_et_reinscrits(jTable_Inscription);
                } else {
                    JOptionPane.showMessageDialog(null, "Réinscription faite avec succès");
                    vider_champs_inscription();
                    new IMP_Inscription().liste_des_inscrits_et_reinscrits(jTable_Inscription);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Inscription Echouée");
            }

        }


    }//GEN-LAST:event_btn_ajout_inscripActionPerformed

