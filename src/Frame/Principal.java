    private void btn_ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajouterActionPerformed
        if (tb_matricule.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Le matricule est Obligatoire ");
            tb_matricule.requestFocus();
        } else if (tb_nom.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Le Nom est Obligatoire ");
            tb_nom.requestFocus();
        } else if (tb_prenom.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Le Prenom est Obligatoire ");
            tb_prenom.requestFocus();
        } else if (cbo_genre.getSelectedItem().toString().equalsIgnoreCase("Selectionner")) {
            JOptionPane.showMessageDialog(null, "Le Genre est Obligatoire ");
            cbo_genre.requestFocus();
        } else if (tb_tel.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Le Telephone est Obligatoire ");
            tb_tel.requestFocus();
        } else {
            Apprenants appren = new Apprenants();
            appren.setMatricule(tb_matricule.getText());
            appren.setNom(tb_nom.getText());
            appren.setPrenom(tb_prenom.getText());
            appren.setGenre(cbo_genre.getSelectedItem().toString());
            appren.setTel(tb_tel.getText());
            appren.setChemin_photo(path);

            int enreg = new IMP_Apprenants().Insertion_Apprenants(appren);
            if (enreg != 1062) {
                if (enreg != 0) {
                    JOptionPane.showMessageDialog(null, "Enregistrement effectuer avec succès");

                    new IMP_Apprenants().charge_combo_matricule(cbo_matricule);
                    vider_champs_apprenant();
                    new IMP_Apprenants().liste_des_apprenants(jTable_Apprenant);
                    new IMP_Apprenants().liste_des_apprenants(jTable_Impression);
                } else {
                    JOptionPane.showMessageDialog(null, "Enregistrement echouer");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cet apprenant existe deja ");
            }

        }


    }//GEN-LAST:event_btn_ajouterActionPerformed

