    private void btn_ajout_coursActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajout_coursActionPerformed
        boolean bol = new IMP_Cours().verifi_cours(tb_nom_cours.getText());
        if (bol) {
            JOptionPane.showMessageDialog(null, "Cours existe deja");
        } else if (tb_nom_cours.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Veuillez saisir le nom du cours");
        } else {
            Cours cour = new Cours();
            cour.setLibelCours(tb_nom_cours.getText());
            boolean enreg = new IMP_Cours().AjouterCours(cour);
            if (enreg) {
                JOptionPane.showMessageDialog(null, "Cours ajouter avec succès");
                new IMP_Cours().charge_combo_cours(cbo_cours);
                new IMP_Cours().charge_combo_cours(cbo_cours_frais_cours);
                new IMP_Cours().charge_combo_cours(cbo_cours_fiche);
                vider_champs_cours();
                new IMP_Cours().Liste_des_cours(jTable_cours);
            } else {
                JOptionPane.showMessageDialog(null, "Echec d'ajout de cours");

            }
        }


    }//GEN-LAST:event_btn_ajout_coursActionPerformed

