    private void jButton49ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton49ActionPerformed
        if (cbo_cours_fiche.getSelectedItem().toString().equalsIgnoreCase("Selectionner")) {
            JOptionPane.showMessageDialog(null, "Le Cours est Obligatoire");
        } else if (tb_nbrejour.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Le Nombre de jour est Obligatoire");
            tb_nbrejour.requestFocus();
        } else if (tb_heure_seance.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "L'heure par seance est Obligatoire");
            tb_heure_seance.requestFocus();
        } else if (tb_duree.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "La durée est Obligatoire");
            tb_duree.requestFocus();
        } else {
            int enreg = new IMP_Fiche().Enregistrer_fiche(cbo_cours_fiche.getSelectedItem().toString(), tb_nbrejour.getText(), tb_heure_seance.getText(), tb_duree.getText(), cbo_cours_fiche.getSelectedItem().toString());
            if (enreg != 1062) {
                if (enreg != 0) {
                    JOptionPane.showMessageDialog(null, "Cours ajouté avec succès dans la fiche");
                    new IMP_Fiche().charge_fiche_renseign(jTable_fiche_renseign);
                    vider_champs_fiche();
                } else {
                    JOptionPane.showMessageDialog(null, "Echec de l'ajout");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ce cours existe dejà dans la fiche");
            }
        }
    }//GEN-LAST:event_jButton49ActionPerformed

